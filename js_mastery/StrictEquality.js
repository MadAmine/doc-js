//  = assignment operator
//  == comparison operator (compare if values are equal)
//  === strict equality operator ( compare if values & datatypes are equal)
//  != inequality operator( it will return True if two values are not equal)
//  !== strict inequality operator

// --------EXAMPLE(==)----------
const PI = 3.14;
if (PI == "3.14"){
     console.log('that is pi');
}else{
     console.log('that is NOT PI');
}// output : that is pi

// --------EXAMPLE(===)----------

if (PI === "3.14"){
     console.log('that is pi');
}else{
     console.log('that is NOT PI');
}// output : that is NOT PI


// --------EXAMPLE(!=)----------

if (PI != "3.14"){
     console.log('that is not pi');
}else{
     console.log('that is PI');
}// output : that is NOT PI



// --------EXAMPLE(!=)----------

if (PI !== "3.14"){
     console.log('that is not pi');
}else{
     console.log('that is PI');
}// output : that is NOT PI

