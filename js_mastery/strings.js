// Strings
var greeting = "Hello, world!";
var name = "John";
var message = `Welcome, ${name}!`; // Template literals allow interpolation

console.log(greeting); // Output: Hello, world!
console.log(message); // Output: Welcome, John!

// Numbers
var num1 = 10;
var num2 = 5;
var sum = num1 + num2;
var product = num1 * num2;

console.log("Sum:", sum); // Output: Sum: 15
console.log("Product:", product); // Output: Product: 50

// Booleans
var isTrue = true;
var isFalse = false;

// Boolean operations
var result1 = true && false; // Logical AND
var result2 = true || false; // Logical OR
var result3 = !true; // Logical NOT

console.log("Result 1:", result1); // Output: Result 1: false
console.log("Result 2:", result2); // Output: Result 2: true
console.log("Result 3:", result3); // Output: Result 3: false
